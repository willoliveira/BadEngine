let Config = {
	FPS: 60
}

//camera
let cameraPosX: number = 1;
let cameraPosY: number = 1;
let cameraSizeWidth: number = 12;
let cameraSizeHeight: number = 12;
let offsetX: number = Math.floor(cameraSizeWidth/2) - cameraPosX;
let offsetY: number = Math.floor(cameraSizeHeight/2) - cameraPosY;


let player: Player;

function init() {
	canvas.width = tileSize * cameraSizeWidth;
	canvas.height = tileSize * cameraSizeHeight;

	image.src = "/assets/tiles.png";
	image.addEventListener("load", () => {
		setInterval(GameLoop, 1000 / Config.FPS);
	});

	player = new Player({ x: cameraPosX + offsetX, y: cameraPosY + offsetY }, 1);
}

function GameLoop() {
	render();
}

function onMoveTo(pos: MovementDirection) {
	let positionRequest: Position = {
		x: cameraPosX + pos.x,
		y: cameraPosY + pos.y
	};

	if (!hasCollision(positionRequest)) {
		player.position.x = cameraPosX += pos.x;
		player.position.y = cameraPosY += pos.y;

		offsetX -= pos.x;
		offsetY -= pos.y;
	}
}

function hasCollision(position: Position) {
	// Vai sair do mapa
	if (position.y < 0 || position.y >= mapCollisions.length  ||
		position.x < 0 || position.x >= mapCollisions[0].length) {
		return true;
	}
	// 1 é colisão
	if (mapCollisions[position.y][position.x] === 1) {
		return true;
	}
	return false
}

function render() {

	for (let layer = 0; layer < mapLayers.length; layer++) {

		for (let row = 0; row < cameraSizeHeight; row++) {
			for (let col = 0; col < cameraSizeWidth; col++) {

				let posY = cameraPosY + row - (cameraSizeHeight / 2);
				let posX = cameraPosX + col - (cameraSizeWidth / 2);

				let imageSrc, widthSrc, heightSrc, widthDist, heightDist;

				widthDist = col * tileSize;
				heightDist = row * tileSize;

				if (posX < 0 || posY < 0 ||
					posX >= mapLayers[0][0].length || posY >= mapLayers[0].length) {
					imageSrc = imageBlank;
					widthSrc = 0;
					heightSrc = 0;
				} else {
					let tileNum = mapLayers[layer][posY][posX];
					imageSrc = image;
					widthSrc = ((tileNum - 1) % (image.width / tileSize));
					heightSrc = Math.floor((tileNum- 1) / (image.width / tileSize));
				}

				if (layer + 1 === player.layer) {
					ctx.drawImage(imageBlank,
						0, 0,
						tileSize, tileSize,

						(cameraPosX + offsetX) * tileSize, (cameraPosY + offsetY) * tileSize,
						tileSize, tileSize
					)
				}

				ctx.drawImage(
					imageSrc,
					// na imagem
					widthSrc * tileSize, heightSrc * tileSize,
					tileSize, tileSize,

					//no canvas
					col * tileSize, row * tileSize,
					tileSize, tileSize);
			}
		}
	}
}


function onKeyPress(evt: any) {
	let dir: MovementDirection = { x: Direction.Idle, y: Direction.Idle };
	//left
	if (evt.keyCode === 37) {
		dir.x = -1;
	}
	//right
	if (evt.keyCode === 39) {
		dir.x = 1;
	}
	//down
	if (evt.keyCode === 40) {
		dir.y = 1;
	}
	//up
	if (evt.keyCode === 38) {
		dir.y = -1;
	}

	onMoveTo(dir);
}

window.addEventListener("keydown", onKeyPress)

