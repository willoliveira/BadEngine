let cameraPosX: number = 1;
let cameraPosY: number = 1;
let cameraSizeWidth: number = 12;
let cameraSizeHeight: number = 12;

function init() {
	canvas.width = tileSize * cameraSizeWidth;
	canvas.height = tileSize * cameraSizeHeight;

	image.src = "/assets/tiles.png";
	image.addEventListener("load", () => {
		setInterval(GameLoop, (1/60) * 1000);
	});

}

function GameLoop() {
	update();
}

function onMoveTo(pos: MovementDirection) {
	cameraPosX += pos.x;
	cameraPosY += pos.y;
}


function update() {

	for(let layer = 0; layer < mapLayers.length; layer++) {

		for(let row = 0; row < cameraSizeHeight + 1; row++) {
			for(let col = 0; col < cameraSizeWidth + 1; col++) {

				let posY = cameraPosY + row - (cameraSizeHeight / 2);
				let posX = cameraPosX + col - (cameraSizeWidth / 2);

				let imageSrc, widthSrc, heightSrc, widthDist, heightDist;

				widthDist = col * tileSize;
				heightDist = row * tileSize;

				if (posX < 0 || posY < 0 ||
					posX >= mapLayers[0][0].length || posY >= mapLayers[0].length) {
					imageSrc = imageBlank;
					widthSrc = 0;
					heightSrc = 0;
				} else {
					let tileNum = mapLayers[layer][posY][posX];
					imageSrc = image;
					widthSrc = ((tileNum - 1) % (image.width / tileSize));
					heightSrc = Math.floor((tileNum- 1) / (image.width / tileSize));
				}

				ctx.drawImage(
					imageSrc,
					// na imagem
					widthSrc * tileSize, heightSrc * tileSize,
					tileSize, tileSize,

					//no canvas
					col * tileSize, row * tileSize,
					tileSize, tileSize);
			}
		}
	}
}


function onKeyPress(evt: any) {
	let dir: MovementDirection = { x: Direction.Idle, y: Direction.Idle };
	//left
	if (evt.keyCode === 37) {
		dir.x = -1;
	}
	//right
	if (evt.keyCode === 39) {
		dir.x = 1;
	}
	//down
	if (evt.keyCode === 40) {
		dir.y = 1;
	}
	//up
	if (evt.keyCode === 38) {
		dir.y = -1;
	}

	onMoveTo(dir);
}

window.addEventListener("keydown", onKeyPress)

window.addEventListener("load", init);